	private PullRequestBehavior BuildPullRequestBehavior(PullRequestSettings? repositoryPullRequestSettings, IReadOnlyList<AppliedConvention> appliedConventions, ApplyCommandSettings applySettings)
	{
		var contributingConventions = GetContributingConventions(appliedConventions);
		var draft = applySettings.Draft
			?? repositoryPullRequestSettings?.Draft
			?? contributingConventions.Any(static x => x.PullRequest?.Draft is true);

		bool autoMergeEnabled;
		if (applySettings.AutoMerge is { } cliAutoMerge)
		{
			autoMergeEnabled = cliAutoMerge;
		}
		else if (repositoryPullRequestSettings?.AutoMerge is { } repositoryAutoMerge)
		{
			autoMergeEnabled = repositoryAutoMerge;
		}
		else
		{
			var explicitConventionAutoMerge = contributingConventions
				.Where(static x => x.PullRequest?.AutoMerge is not null)
				.Select(static x => x.PullRequest!.AutoMerge!.Value)
				.ToList();

			if (explicitConventionAutoMerge.Contains(false))
			{
				autoMergeEnabled = false;
			}
			else if (explicitConventionAutoMerge.Contains(true))
			{
				autoMergeEnabled = true;
			}
			else if (contributingConventions.Count > 0 && contributingConventions.All(static x => !x.HasExecutableScript))
			{
				autoMergeEnabled = true;
			}
			else
			{
				autoMergeEnabled = false;
			}
		}

		var conventionMergeMethods = contributingConventions
			.Select(static x => x.PullRequest?.MergeMethod)
			.Where(static x => !string.IsNullOrWhiteSpace(x))
				.Select(ParseMergeMethod)
				.Distinct(StringComparer.Ordinal)
				.ToList();
		var mergeMethodConflictFallback = conventionMergeMethods.Count > 1 && applySettings.MergeMethod is null && string.IsNullOrWhiteSpace(repositoryPullRequestSettings?.MergeMethod);
		var desiredMergeMethod = applySettings.MergeMethod is { } cliMergeMethod
			? ParseMergeMethod(cliMergeMethod)
			: !string.IsNullOrWhiteSpace(repositoryPullRequestSettings?.MergeMethod)
				? ParseMergeMethod(repositoryPullRequestSettings!.MergeMethod!)
				: conventionMergeMethods.Count == 1
					? conventionMergeMethods[0]
					: "squash";

		var labels = new List<string>();
		AddUnique(labels, c_repoConventionsLabel, StringComparer.OrdinalIgnoreCase);
		AddUniqueRange(labels, repositoryPullRequestSettings?.Labels, StringComparer.OrdinalIgnoreCase);
		AddUniqueRange(labels, contributingConventions.SelectMany(static x => x.PullRequest?.Labels ?? []), StringComparer.OrdinalIgnoreCase);

		var reviewers = new List<string>();
		var assignees = new List<string>();
		if (!autoMergeEnabled)
		{
			AddUniqueRange(reviewers, repositoryPullRequestSettings?.Reviewers, StringComparer.OrdinalIgnoreCase);
			AddUniqueRange(reviewers, contributingConventions.SelectMany(static x => x.PullRequest?.Reviewers ?? []), StringComparer.OrdinalIgnoreCase);

			AddUniqueRange(assignees, repositoryPullRequestSettings?.Assignees, StringComparer.OrdinalIgnoreCase);
			AddUniqueRange(assignees, contributingConventions.SelectMany(static x => x.PullRequest?.Assignees ?? []), StringComparer.OrdinalIgnoreCase);
		}

		return new PullRequestBehavior(labels, reviewers, assignees, draft, autoMergeEnabled, desiredMergeMethod, mergeMethodConflictFallback, applySettings.AutoMerge is true);
	}

