	private async Task<HashSet<string>> GetRepositoryLabelsAsync(CancellationToken cancellationToken)
	{
		var result = await RunExternalCommandAsync("gh", m_settings.TargetRepositoryRoot, ["label", "list", "--limit", "1000", "--json", "name"], cancellationToken);
		if (result.ExitCode != 0)
			throw new InvalidOperationException($"Failed to query labels with gh: {result.StandardError}{result.StandardOutput}");

		try
		{
			var labels = JsonSerializer.Deserialize<IReadOnlyList<GitHubLabel>>(string.IsNullOrWhiteSpace(result.StandardOutput) ? "[]" : result.StandardOutput, s_pullRequestJsonSerializerOptions) ?? [];
			return labels.Select(static x => x.Name).ToHashSet(StringComparer.OrdinalIgnoreCase);
		}
		catch (JsonException ex)
		{
			throw new InvalidOperationException($"Failed to parse labels from gh: {result.StandardOutput}{result.StandardError}", ex);
		}
	}

